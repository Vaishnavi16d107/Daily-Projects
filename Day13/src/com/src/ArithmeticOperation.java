package com.src;
public class ArithmeticOperation {
	
	    //add method accepts two numbers as arguments and returns its summation.
	    public int add(int number1,int number2)
	   {
		    return number1+number2;
	   }
	
	    //mul method accepts two numbers as arguments and returns its product.
	    public int multiplication(int number1,int number2)
	   {
		     return number1*number2;
	   }

}
